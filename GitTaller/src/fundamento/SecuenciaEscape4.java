//Ejercicio sobre secuencia de escape para imprimir comillas, barra invertida y cortar en lineas

public class SecuenciaEscape4 {
	public static void main(String[] args){
	//Imprimiendo la palabra mundo entre comillas
	System.out.println("Hola \"Mundo\"");	

	//Imprimiendo la palabra mundo entre barras invertidas
	System.out.println("Hola \\Mundo\\");

	//Imprimiendo la palabra mundo debajo de la palabra Hola
	System.out.println("Hola \nMundo");

	
	}
}